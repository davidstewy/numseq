"""
Module containing 3 different geometric functions, 
square(n), triangle(n), and cube(n).
"""


def square(n):
    """Returns the square of n, which is n * n."""
    return n * n


def triangle(n):
    """Returns the triangle of n, which is n * (n+1) / 2."""
    return n*(n+1)/2


def cube(n):
    """Returns the cube of n which is n * n * n."""
    return n * n * n
